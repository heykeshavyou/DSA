#include <iostream>
using namespace std;
int main(){



    //Example of Type Conversion (Implicit)
    char a ='A';
    cout<<"Char: "<<a<<endl;
    int b=a;
    cout<<"Int: "<<b<<endl;



    //Example of Type Casting (Explicit)
    double num1 = 12.9999;
    cout<<"Double: "<<num1<<endl;
    int num2 = (int)num1;
    cout<<"Int: "<<num2<<endl;




    return 0;
}